# Problem 2 : Odd Or Even
# 
# Input
# Ask the user for a number. 
# 
# Output
# Depending on whether the number is even or odd, print out an message to the user.

# In[11]:

input_number = input("Please input a number :")

if int(input_number) % 2 == 0:
    print (input_number + " is even number")
else:
    print (input_number + " is odd number")


